package main;

public class valueType {
    public static void main(String[] args) {
        int a = 0b0101; // 2진수
        int b = 0206; // 8진수
        int c = 365; // 10진수
        int d = 0x12; // 16진수

        System.out.println("a : " + a);
        System.out.println("b : " + b);
        System.out.println("c : " + c);
        System.out.println("d : " + d);
    }
}
